// =============================================================================
// UI_AutoTest.js — простой автотест: клик по элементам на странице (для запуска в консоли DevTools)
// =============================================================================
// Назначение: находит ссылку «Рейтинг» (href="/rating") и программно нажимает на неё.
// Запуск: вставить код в консоль (F12) на открытой странице приложения.
// =============================================================================

(function () {
  // Селектор ссылки «Рейтинг» (как в разметке: <a href="/rating" ...>)
  const selector = 'a[href="/rating"]';

  const el = document.querySelector(selector);
  if (!el) {
    console.warn("UI_AutoTest: элемент не найден, селектор:", selector);
    return;
  }

  console.log("UI_AutoTest: клик по элементу", el);
  el.click();
})();
